/**
 * Function to compare 2 strings;
 * returns TRUE if strings are equal, otherwise FALSE
 * 
 * @param {string} old value;
 * @param {string} new value;
 */
function compareValues_(oldvalue, newvalue){
  if (newvalue === oldvalue) {
    return true;
  } else {
    return false;
  }
}
